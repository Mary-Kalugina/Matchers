import rangeHealth from '../../../index';

test('Сортировка персонажей по состоянию', () => {
  const input = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const received = rangeHealth(input);

  expect(received).toEqual(expected);
});
